namespace app\common\model;
use think\Model;
class GroupRedBuy extends Model {
    public function goods(){
        return $this->hasOne('goods_red','goods_id','goods_id');
    }
    public function specGoodsPrice(){
        return $this->hasOne('specRedGoodsPrice','item_id','item_id');
    }
    //剩余团购库存
    public function getStoreCountAttr($value, $data)
    {
        return $data['goods_num'] - $data['buy_num'];
    }
}
